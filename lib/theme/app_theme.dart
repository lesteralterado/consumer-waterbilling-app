  /// Status color helpers
  static Color getStatusColor(String status, {required bool isLight}) {
    switch (status.toLowerCase()) {
      case 'paid':
      case 'completed':
      case 'resolved':
        return isLight ? successLight : successDark;
      case 'pending':
      case 'processing':
        return isLight ? warningLight : warningDark;
      case 'overdue':
      case 'failed':
      case 'error':
        return isLight ? errorLight : errorDark;
      default:
        return isLight ? textSecondaryLight : textSecondaryDark;
    }
  }

  /// Elevation shadows for cards and surfaces
  static List<BoxShadow> getElevationShadow({
    required bool isLight,
    double elevation = 2.0,
  }) {
    if (elevation <= 0) return [];

    final Color shadowColor = isLight ? shadowLight : shadowDark;
    final double blur = elevation * 2;
    final double offset = elevation;

    return [
      BoxShadow(
        color: shadowColor,
        blurRadius: blur,
        offset: Offset(0, offset),
